    # –°–æ–∑–¥–∞–Ω–∏–µ –∏ –∑–∞–ø—É—Å–∫ screen-—Å–µ—Å—Å–∏–∏ —Å –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ–º –∫–æ–º–∞–Ω–¥—ã —É—Å—Ç–∞–Ω–æ–≤–∫–∏
    screen -dmS nexus bash -c 'curl https://cli.nexus.xyz/ | sh'

    echo -e "${CLR_SUCCESS}‚úÖ –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞! –£–∑–µ–ª –∑–∞–ø—É—â–µ–Ω –≤ screen-—Å–µ—Å—Å–∏–∏ 'nexus'.${CLR_RESET}"
